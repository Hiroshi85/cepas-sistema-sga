    public void setAlumno(Alumno alumno){
        a = alumno;
        llenarDatos();
        try {
            listaCursos = CursoDAO.getInstancia().mostrarPorGrado(m.getGrado().getIdGrado());
            llenarCursos();
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR: "+e.getMessage(),"",0);
        }
        
    }
    private void llenarCursos(){
        for(int i=0; i<listaCursos.size();i++){
            cboCurso.addItem(listaCursos.get(i).getNombre());
        }
    }
    private void llenarDatos(){
        lblAlumno.setText(a.getApellido_p()+" "+a.getApellido_m()+", "+a.getNombre());
        try {
            m = MatriculaDAO.getInstancia().buscarMatriculaPorDniAlumno(a.getDni());
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR: "+e.getMessage(),"",0);
        }
        lblGrado.setText(String.valueOf(m.getGrado().getNumGrado()));
        lblSeccion.setText(m.getGrado().getSeccion());
    }
    
