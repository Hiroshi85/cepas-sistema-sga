    private void cboBimestreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboBimestreActionPerformed
        // TODO add your handling code here:
        try {
            String nombreCursoSelect = cboCurso.getSelectedItem().toString();
            int pos=-1;
            for (int i = 0; i < listaCursos.size(); i++) {
                if(listaCursos.get(i).getNombre().equalsIgnoreCase(nombreCursoSelect))
                    pos = i;
            }
            if(pos!=-1){
                int cboSelect = cboBimestre.getSelectedIndex();
                if(cboSelect == 0)
                    limpiarDatos();
                else{ 
                    Bimestre bimestre = BimestreDAO.getInstancia().buscar(a.getDni(),listaCursos.get(pos).getIdCurso(), cboSelect);
                    if(bimestre!=null)
                        llenarAsistencia_Notas(bimestre);
                }
            }else {
                JOptionPane.showMessageDialog(null, "Seleccione un curso","REPORTE DE NOTAS",2);
                cboBimestre.setSelectedIndex(0);
            }
        }  catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR: "+e.getMessage(),"",0);
        }
    }//GEN-LAST:event_cboBimestreActionPerformed

    private void cboCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboCursoActionPerformed
        // TODO add your handling code here:
        if(cboCurso.getSelectedIndex() == 0)
            limpiarDatos();
        else {
            try {
                String nombreCursoSelect = cboCurso.getSelectedItem().toString();
                int pos = -1;
                for (int i = 0; i < listaCursos.size(); i++) {
                    if (listaCursos.get(i).getNombre().equalsIgnoreCase(nombreCursoSelect)) {
                        pos = i;
                    }
                }
                if (pos != -1) {
                    int cboSelect = cboBimestre.getSelectedIndex();
                    
                        Bimestre bimestre = BimestreDAO.getInstancia().buscar(a.getDni(), listaCursos.get(pos).getIdCurso(), cboSelect);
                        if (bimestre != null) {
                            llenarAsistencia_Notas(bimestre);
                        }
                    
                } else {
                    JOptionPane.showMessageDialog(null, "Seleccione un curso", "REPORTE DE NOTAS", 2);
                    cboBimestre.setSelectedIndex(0);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR: " + e.getMessage(), "", 0);
            }
        }
    }//GEN-LAST:event_cboCursoActionPerformed

