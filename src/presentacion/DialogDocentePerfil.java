    private void llenarDatos(){
        txtDNI.setText(d.getDni());        
        txtApMaterno.setText(d.getApellido_m());
        txtApPaterno.setText(d.getApellido_p());
        txtNombres.setText(d.getNombre());
        txtEdad.setText(String.valueOf(d.getEdad()));
        txtSexo.setText(d.getSexo());
        txtTelefono.setText(d.getTelefono());
        txtFechaNac.setText(d.getFecha_nacimiento());
        
        txtSueldo.setText(String.format(Locale.US,"%.2f",d.getSueldo()));
        passAux=d.getPassword();
        dniAux=txtDNI.getText();
    }
