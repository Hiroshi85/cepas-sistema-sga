    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String pword = txtNuevaContraseña.getText().trim();
        
        try {
            pword = ENC.SHA256(pword);
            //Docente docente = new Docente(d.getSueldo(),d.getDni(), pword, d.getNombre(),d.getApellido_p(), d.getApellido_m(), d.getSexo(), d.getEdad(), d.getTelefono(), d.getFecha_nacimiento());
            DocenteDAO.getInstancia().actualizarPass(d.getDni(), pword);
            
            limpiar();
            desactivar();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: "+e.getMessage());
        } catch (NoSuchAlgorithmException ex) {
            System.out.println("Error: "+ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

